     public Bookmarksinfo(int bookmarkId) {
        initComponents(); // Initialize the components

        try {
            // Connect to your MySQL database
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/bookstore", "root", "12345678");

            // Prepare a SQL statement to select the bookmark information based on bookmarkId
            String sql = "SELECT * FROM Bookmarks WHERE id = ?";
            try (PreparedStatement statement = connection.prepareStatement(sql)) {
                statement.setInt(1, bookmarkId);

                // Execute the query
                ResultSet resultSet = statement.executeQuery();

                // Check if the result set has a row
                if (resultSet.next()) {
                    // Set the fetched information to the UI components
                    jLabel1.setText(resultSet.getString("name"));
                    // Note: BLOB handling depends on how you store images. You may need to modify this part accordingly.
                    // Assuming you stored images as BLOBs and use javax.swing.ImageIcon
                    byte[] imageBytes = resultSet.getBytes("bigimage");
                    if (imageBytes != null) {
                        jLabel3.setIcon(new javax.swing.ImageIcon(imageBytes));
                    }
                    jTextArea1.setText(resultSet.getString("description"));
                    jLabel5.setText(String.valueOf(resultSet.getInt("price")));
                    jTextField5.setText(resultSet.getString("availability"));
                    jTextField6.setText(resultSet.getString("Theme"));
                    jTextField7.setText(resultSet.getString("Delivery"));
                } else {
                    // Handle the case where no bookmark with the given ID is found
                    System.out.println("Bookmark not found!");
                }
            }

            // Close the connection
            connection.close();
        } catch (SQLException e) {
            e.printStackTrace();
            // Handle SQLException appropriately
        }
    }

