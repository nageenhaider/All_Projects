    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         // Get the entered name from the JTextField
    String enteredName = jTextField1.getText();

    // Connect to the database and retrieve bookmark information
    try (Connection connection = ConnectionProvider.getCon()) {
        // Query to retrieve bookmark information based on the entered name
        String query = "SELECT name, description, price, availability, Theme, Delivery, bigimage FROM Bookmarks WHERE name = ?";

        try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
            preparedStatement.setString(1, enteredName);

            try (ResultSet resultSet = preparedStatement.executeQuery()) {
                // Check if any result is returned
                if (resultSet.next()) {
                    // Display the retrieved information in the corresponding UI components

                    // Set JLabels
                    jLabel1.setText(resultSet.getString("name"));
                    jLabel3.setIcon(displayImage(resultSet.getBlob("bigimage")));
                    jLabel5.setText(resultSet.getString("price"));
                    jTextField5.setText(resultSet.getString("availability"));
                    jTextField6.setText(resultSet.getString("Theme"));
                    jTextField7.setText(resultSet.getString("Delivery"));

                    // Set JTextArea
                    jTextArea1.setText(resultSet.getString("description"));
                } else {
                    // If no result is found, you may want to display a message or handle it accordingly
                    System.out.println("Bookmark not found.");
                    JOptionPane.showMessageDialog(null, "Bookmark Not Found.");
                }
            }
        }

    } catch (SQLException e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton2ActionPerformed
private ImageIcon displayImage(Blob blob) throws SQLException {
    if (blob != null) {
        byte[] bytes = blob.getBytes(1, (int) blob.length());
        return new ImageIcon(bytes);
    } else {
        // Set a default image or handle it accordingly
        return null;
    }
}
